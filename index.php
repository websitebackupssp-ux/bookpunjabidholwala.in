      <script>
        (function() {
          const container = document.getElementById('yt-gallery');
          if (!container) return;

          function loadIframe(card) {
            const src = card.getAttribute('data-embed-src');
            if (!src) return;
            // build iframe
            const wrapper = document.createElement('div');
            wrapper.className = 'ratio-iframe';
            const iframe = document.createElement('iframe');
            iframe.setAttribute('src', src);
            iframe.setAttribute('allow', 'accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture');
            iframe.setAttribute('allowfullscreen', '');
            iframe.setAttribute('title', card.getAttribute('aria-label') || 'YouTube video');
            iframe.setAttribute('loading', 'lazy');
            wrapper.appendChild(iframe);
            // replace card content
            card.innerHTML = '';
            card.appendChild(wrapper);
          }

          container.addEventListener('click', function(e) {
            const card = e.target.closest('.video-card');
            if (!card) return;
            loadIframe(card);
          });

          // keyboard support (Enter / Space to play)
          container.addEventListener('keydown', function(e) {
            if (e.key === 'Enter' || e.key === ' ') {
              const card = e.target.closest('.video-card');
              if (!card) return;
              e.preventDefault();
              loadIframe(card);
